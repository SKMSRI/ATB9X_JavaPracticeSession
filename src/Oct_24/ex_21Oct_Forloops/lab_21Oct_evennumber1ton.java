package Oct_24.ex_21Oct_Forloops;
//Java program to print sum of all even numbers between 1 to n using for loop.
public class lab_21Oct_evennumber1ton {
    public static void main(String[] args) {
        for (int i =1; i > 0; i++)
        {
            if (i % 2 == 0){
                System.out.println(i);
            }
        }
    }
}
