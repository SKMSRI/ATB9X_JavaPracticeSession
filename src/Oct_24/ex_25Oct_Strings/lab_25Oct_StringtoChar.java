public class lab_25Oct_StringtoChar {
    public static void main(String[] args) {

        String str = "SAURABH";
        char[] chars = str.toCharArray();
        System.out.println(chars);
        for (int i= 0; i< chars.length; i++) {
            System.out.println(chars[i]);
        }
    }
}
