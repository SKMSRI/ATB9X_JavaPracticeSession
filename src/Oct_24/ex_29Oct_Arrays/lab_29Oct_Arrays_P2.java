package Oct_24.ex_29Oct_Arrays;

//By using New operator
public class lab_29Oct_Arrays_P2 {
    public static void main(String[] args) {
        int[] marks_2 = new int[6];
        System.out.println(marks_2.length);

        marks_2[0] = 11;
        marks_2[1] = 12;
        marks_2[3] = 14;
        marks_2[5] = 18;

        System.out.println(marks_2[0]);
        System.out.println(marks_2[3]);
        System.out.println(marks_2[5]);

    }
}
